package pkg00;

public class Add_pattern {
	public static void main(String[] args) {
		
		for(int i=1;i<=7;i++)
		{
			for(int k=7;k>=i;k--)
			{
				System.out.print("*");
			}
			
			for(int j=1;i<i;j++)
			{
				System.out.print(" ");
			}
			
			System.out.println();
		}
		{
			
		}
	}

}
